# Exercise 2: Print the sum of the current and previous numbers.

# Getting the 10 numbers.

previous_number = 0

for i in range (0, 10):
    # In each input, get the sum of the current and previous numbers.
    sum = previous_number + i

    print ("Current Number + Previous Number: ", i, " + ", previous_number, " = ", sum)
    previous_number = i








